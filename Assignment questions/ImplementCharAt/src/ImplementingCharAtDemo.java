
public class ImplementingCharAtDemo {

public static void main(String args[]){
		
		String inputString = "Input string";
		System.out.println("Character at 4th index : "+inputString.charAt(4));
		char arrayOfCharacters[] = inputString.toCharArray();
		System.out.println("Character at 4th index : "+arrayOfCharacters[4]);
		
	}
}
